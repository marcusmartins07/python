# sum = realiza soma dos elementos

lista = [1, 8, 4, 99, 34, 129]
print(sum(lista))

tupla = (1, 8, 4, 99, 34, 129)
print(sum(tupla))

conjunto = {1, 8, 4, 99, 34, 129}
print(sum(conjunto))

dicionario = {'a': 1, 'b': 8, 'c': 4, 'd': 99, 'e': 34, 'f': 129}
print(sum(dicionario.values()))

